	INSERT INTO [dbo].[Article]
		(
	 [Title],
	 [Content],
	 [Timestamp],
	 [NewsSourceID],
	 [Url],
	 [NewsTime]
		)
	SELECT 		
			 @Title,
	 @Content,
	 @Timestamp,
	 @NewsSourceID,
	 @Url,
	 @NewsTime
END

